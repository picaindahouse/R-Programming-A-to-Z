# This lesson we'll be learning about logical Variables
# So as learnt before:
# In R- TRUE & T, FALSE & F

3==4
3!=4
3<4
3>4
3<=4
3>=4

# All of the above are pretty much the same as python

# Some special stuff:
# | is the same as or in python
# & is the same as and in python
tom = T
tim = F
tom | tim
tom & tim

# Can use ! in some cool ways as well
tom # We said it equal T
!tom
# Basically it acts like a negative sign
# Makes true false and false true
! tim
